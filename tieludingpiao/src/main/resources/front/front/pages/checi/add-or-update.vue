            },
            // 登陆用户信息
            user: {},
            checiTypesOptions: [],
            checiTypesIndex : 0,
            shangxiaTypesOptions: [],
            shangxiaTypesIndex : 0,

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
		/*下拉框*/
			let checiTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'checi_types',
            }
			let checiTypes = await this.$api.page(`dictionary`, checiTypesParams);
			this.checiTypesOptions = checiTypes.data.list
		/*下拉框*/
			let shangxiaTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'shangxia_types',
            }
			let shangxiaTypes = await this.$api.page(`dictionary`, shangxiaTypesParams);
			this.shangxiaTypesOptions = shangxiaTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`checi`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.checiId){
                this.ruleForm.checiId = options.checiId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            checiTypesChange(e) {
                this.checiTypesIndex = e.target.value
                this.ruleForm.checiValue = this.checiTypesOptions[this.checiTypesIndex].indexName
                this.ruleForm.checiTypes = this.checiTypesOptions[this.checiTypesIndex].codeIndex
            },
            shangxiaTypesChange(e) {
                this.shangxiaTypesIndex = e.target.value
                this.ruleForm.shangxiaValue = this.shangxiaTypesOptions[this.shangxiaTypesIndex].indexName
                this.ruleForm.shangxiaTypes = this.shangxiaTypesOptions[this.shangxiaTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			checiPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.checiPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			checiTimeConfirm(val) {
                this.ruleForm.checiTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.checiName)) {
                    this.$utils.msg(`车次标题不能为空`);
                    return
                }
				if ((!this.ruleForm.checiPhoto)) {
                    this.$utils.msg(`火车照片不能为空`);
                    return
                }
				if ((!this.ruleForm.checiChufadi)) {
                    this.$utils.msg(`出发地不能为空`);
                    return
                }
				if ((!this.ruleForm.checiMudidi)) {
                    this.$utils.msg(`目的地不能为空`);
                    return
                }
				if ((!this.ruleForm.checiTime)) {
                    this.$utils.msg(`出发时间不能为空`);
                    return
                }
				if ((!this.ruleForm.sectionNumber) && (!this.$validate.isIntNumer(this.ruleForm.sectionNumber)) && this.ruleForm.sectionNumber >0) {
                    this.$utils.msg(`车厢不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.zuoweiNumber) && (!this.$validate.isIntNumer(this.ruleForm.zuoweiNumber)) && this.ruleForm.zuoweiNumber >0) {
                    this.$utils.msg(`座位不能为空，不能小于0 格式为数字`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`checi`, this.ruleForm);
                } else {
                    await this.$api.save(`checi`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
</script>
