        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
            let checiParams = {
                page: 1,
                limit: 100,
            }
            let checiData = await this.$api.page(`checi`, checiParams);
            this.checiOptions = checiData.data.list;
            let yonghuParams = {
                page: 1,
                limit: 100,
            }
            let yonghuData = await this.$api.page(`yonghu`, yonghuParams);
            this.yonghuOptions = yonghuData.data.list;
		/*下拉框*/
			let checiOrderTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'checi_order_types',
            }
			let checiOrderTypes = await this.$api.page(`dictionary`, checiOrderTypesParams);
			this.checiOrderTypesOptions = checiOrderTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`checiOrder`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.checiOrderId){
                this.ruleForm.checiOrderId = options.checiOrderId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            checiChange(e) {
                this.checiIndex = e.target.value
                this.ruleForm.checiName = this.checiOptions[this.checiIndex].checiName
                this.ruleForm.checiId = this.checiOptions[this.checiIndex].id
            },
            yonghuChange(e) {
                this.yonghuIndex = e.target.value
                this.ruleForm.yonghuName = this.yonghuOptions[this.yonghuIndex].yonghuName
                this.ruleForm.yonghuId = this.yonghuOptions[this.yonghuIndex].id
            },
            // 下拉变化
            checiOrderTypesChange(e) {
                this.checiOrderTypesIndex = e.target.value
                this.ruleForm.checiOrderValue = this.checiOrderTypesOptions[this.checiOrderTypesIndex].indexName
                this.ruleForm.checiOrderTypes = this.checiOrderTypesOptions[this.checiOrderTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
